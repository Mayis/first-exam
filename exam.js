function createTrangle(width) {
	let result = "";
	for (let i = 1; i <= width; i++) {
		for (let j = 1; j <= width * 2 - 1; j++) {
			if (j <= width - i || j >= width + i) {
				result += " ";
			} else {
				result += "*";
			}
		}
		result += "\n";
	}
	return result;
}
